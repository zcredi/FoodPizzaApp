import UIKit

extension UIFont {
    //Bold
    static func sfUiDisplayBold13() -> UIFont? {
        return UIFont.init(name: "sf-ui-display-bold", size: 13)
    }
    
    //Semi-Bold
    static func sfUiDisplaySemiBold17() -> UIFont? {
        return UIFont.init(name: "sf-ui-display-semiBold", size: 17)
    }
    
    //Medium
    static func sfUiDisplayMedium17() -> UIFont? {
        return UIFont.init(name: "sf-ui-display-medium", size: 17)
    }
    
    //Regular
    static func sfUiDisplayRegular13() -> UIFont? {
        return UIFont.init(name: "sf-ui-display-regular", size: 13)
    }
}
